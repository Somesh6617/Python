#this operator deals with "in" and "not in"
a=[1,23,4]
print(23 not in a) #it returns True if 23 is not present in list a else returns False
print(1 in a) #it returns True if 1 is present in list a else returns False
print(4 in a and 1 not in a) #using logical operator
print(4 in a or 1 not in a) #using logical operator

